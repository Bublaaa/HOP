import mongoose from "mongoose";

const outpostSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    latitude: {
      type: Number,
      required: true,
    },
    longitude: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export const Outpost = mongoose.model("Outpost", outpostSchema);
